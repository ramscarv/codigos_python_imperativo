# preenchimento de formulário
print("Formulário")
print("Digite 1 para preencher o formulário")
print('Digite 0 para sair')
form = int(input())

while form != 0:
    print("Digite o nome:")
    nome = input()
    print("Digite a idade:")
    idade = int(input())
    print("Digite o estado civil:")
    estado_civil = input()

    print(nome,"\n",idade,"\n",estado_civil)
    print("Formulário")
    print("Digite 1 para preencher o formulário")
    print('Digite 0 para sair')
    form = int(input())
print("sair")

    
      
